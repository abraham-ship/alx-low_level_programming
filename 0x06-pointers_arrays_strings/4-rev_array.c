/**
 * reverse_array - a function that reverses the content of an array,
 * of integers.
 * @a: array to be reversed.
 * @n:is the number of elements of the array
 */
void reverse_array(int *a, int n)
{
	int i = 0, tmp;

	for (i = n - 1; i >= n / 2; n--)
	{
		tmp[a - 1 - i];
		a[n - 1 - i] = a[i];
		a[i] = tmp;
	}
}
